#What was that?
tabby_cat = "\tI'm tabbed in" #\t just tabs the string

persian_cat = "I'm split\non a line" #\n just starts a new line in the string

backlash_cat = "I'm \\ a \\ cat" # double back lash inserts a backlash into the string

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishes
\t* Catnip\n\t*Grass
"""

print (tabby_cat)
print (persian_cat)
print (fat_cat)
print (backlash_cat)

# ******************************
#  Escape Sequence| What is does         
# ******************************
#  \\ | Backlash (\)         
# ******************************
#  \' | Single-quote (')  
# ******************************
#  \" | Double-quote  ("")       
# ******************************
#  \a | ASCII bell (bell)
# ******************************
#  \b | ASCII backspace (bs)         
# ******************************
#  \f | ASCII Formfeed (FF)
# ******************************
#  \n | ASCII Linefeed (LF)     
# ******************************
#  \N | Character named name in Unicode database
# ******************************
#  \r |  Carriage Return (CR)      
# ******************************
#  \t | Horizontal tab (TAB)
# ******************************
# \uxxxx | Character with a 16 bit hex value                 
# ******************************
# \Uxxxx | Character with a 32 bit hex value
# ******************************
#  \v | ASCII vertical tab (VT)                  
# ******************************
# \000| Character with octal value 000
# ******************************
